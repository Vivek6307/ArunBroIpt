package loop_Concepts;

public class BasicForLoop {
	public static void main(String[] args) {
		for (int i = 0; i <= 10; i++) {
			System.out.println(i);
		}
		System.out.println("================================");

		for (int i = 10; i >= 1; i--) {
			System.out.println(i);
		}
		System.out.println("================================");
		for (int i = 0; i <= 10; ++i) {
			System.out.println(i);
		}
	}

}
